for numero in range(1, 101):
    if numero % 3 == 0:
        print("Fizz")
    else:
        print(numero)


for numero in range(1, 101):
    if numero % 5 == 0:
        print("Buzz")
    else:
        print(numero)

for numero in range(1, 101):
    if numero % 3 == 0 and numero % 5 == 0:
        print("FizzBuzz")
    else:
        print(numero)










